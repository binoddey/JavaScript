// The ternary operator is a shorthand version of if...else.
// It is mainly used for short conditions.

// ==========================================

/* Ternary Operator Syntax:

condition ? trueValue : falseValue;

*/

// ==========================================

// EXAMPLE 1: Even or Odd

let number = 7;

let result = (number % 2 === 0)
    ? "Even"
    : "Odd";

console.log(result);