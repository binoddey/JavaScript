// 1. Arithmatic Operators
let a = 10;
let b = 20;
console.log("a + b = ", a + b);
console.log("a - b = ", a - b);
console.log("a * b = ", a * b);
console.log("a / b = ", a / b);
console.log("a % b = ", a % b);

// 2. Assignment Operators
let c = 10;
c += 10;
console.log(c);

// 3. Comparison Operators
console.log(a == b);
console.log(a < b);
console.log(a > b);

// 4. Logical Operators
console.log(a && b);
console.log(a || b);
console.log(!a);
console.log(!b);

// 5. Unary Operators (Increment and Decrement)
console.log(--a);
console.log(--b);
console.log(a--);
console.log(b--);
console.log(a++);
console.log(b++);



