// Rules for naming variables
// 1. Cannot start with number. Can only start with letter, underscore(_), or $
// 2. Are case-sensitive
// 3. Cannot use reserved keywords
// 4. Space not allowed in between.
// 5. Cannot use special characters.

//Types of variables
// 1. var: Variable can be redeclared and updated.
// 2. let: Variable can be updated but not redeclared.
// 3. const: Variable cannot be updated or redeclared.

let name = "Binod";
console.log(name); //Binod
name = "Dey";
console.log(name); //Dey

